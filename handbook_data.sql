THE PLMUN SEAL
The University Seal of the PLMun is the most visible identification mark of the University. Patterned after the official Seal of the City of Muntinlupa; the Torch symbolizes the role of education in the attainment of the City’s Vision: To make Muntinlupa the   Premiere Emerald City of the 21st Century.
Pamantasan ng Lungsod ng Muntinlupa - the name “PAMANTASAN NG LUNGSOD NG MUNTINLUPA” in bold Arial font serves
as a border around the outer portion of the seal.
Torch - the Torch reflects the objective of education: to enlighten the students; and to provide them with the intellectual nurture that draws them away from the elements of destruction.
The Bamboo Seal - Bamboo around the seal projects the resiliency of the Pamantasan ng Lungsod ng Muntinlupa (PLMun) students to the challenges of the times. The bamboo signifies the endurance and the capability of the PLMun students to
overcome trials and emerge victorious in life.
Lakas Talino at Buhay - Lifted from the lyrics of the City’s official anthem, the City of Muntinlupa March, Lakas, Talino at Buhay are the three main objectives which indicate the University’s vision and mission.
1991 - 2003 - the year 1991 marks the time when the Pamantasan ng Lungsod ng Muntinlupa (PLMun) was established, while the year 2003 indicates the time when the Pamantasan ng Lungsod ng Muntinlupa (PLMun), then Muntinlupa Polytechnic
College (MPC), acquired its University status.
The Philippine Flag - the flag that provides the background of the Torch draws the Pamantasan ng Lungsod ng Muntinlupa
(PLMun) into the reality that it is apart of the Republic of the Philippines and the City Government of Muntinlupa. The Torch   scintillates and marks its effects on all areas affected by its radiance. Like education, the University views its significance in the process of nation building.
The Nine Stars - the nine (9) stars represent the nine (9) barangays of the City of Muntinlupa, namely: Tunasan, Poblacion, Putatan, and Bayanan for District I and Alabang, Cupang, Buli, Sucat and Ayala Alabang for District II.
The University Color - the Pamantasan ng Lungsod ng Muntinlupa (PLMun) established bamboo green as its official University color.
Philosophy of the Pamantasan ng Lungsod ng Muntinlupa (PLMun)
Imbued with the provision of the 1987 Constitution which mandates the State to give priority to education, science, technology,
culture and the arts, the Pamantasan ng Lungsod ng Muntinlupa (PLMun) premised its philosophy on the rationale of establishing an educational institution in the tertiary level which reflects on the mandated provisions.
The Pamantasan ng Lungsod ng Muntinlupa (PLMun) aims to promote intellectual growth, to initiate the advancement of
learning and research, to develop responsible and effective leaders, to create esteemed professionals, to enrich the country’s
historical and cultural heritage, to provide quality education for the Muntinlupeños, primarily, and to prepare them to meet the demands of the global market, thus, enabling them to become responsible and employable citizens.
As a learning institution, Pamantasan ng Lungsod ng Muntinlupa’s educational paradigm of curricular programs shows values, academic and technical-orientation aimed at cultivating the mind, spirit and body in the service of God, the country and the
community.
