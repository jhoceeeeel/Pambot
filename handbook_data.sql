Co-Curricular and Extra-Curricular Activities
• The University provides students with the opportunity to participate in properly coordinated co-curricular programs with the following guidelines:
>Co-Curricular Activities (Academic related activities)
>Extra Curricular Activities (Non-academic activities)
• The PLMun provides students the opportunity to participate in well-balanced and properly coordinated co-curricular programs with the following guidelines:
• Action Plan for one year or semester shall be submitted in the Office of the Student   Affairs by all student organizations.
• No student activity shall be held two (2) weeks before the start of final exam to enable students enough time to review for their final exam. As much as possible there shall be no disruption of classes.
• All extra and co-curricular activities that will be held inside the University must be registered with the OSA as follows:
>Secure from the Office of the Student Affairs an application form/permit to hold an activity
>Indicate the nature of the activity and ways to implement the same, in consultation with the faculty advisers
>Have the application form signed by the organization president and endorsed by the faculty adviser
>Submit the accomplished form in duplicate atleast five (5) working days prior to said activity to the Dean and OSA for approval.
•  For co-curricular activities to beheld outside the University, the following procedures shall apply:
>The President of the Organization secures permit from the Office of the Student Affairs
>Accomplishes the application form in duplicate and submits the same to the Office of the Student Affairs
>A waiver/parents consent shall be required of students who wish to join the activities outside the university as well as overnight activities
>The faculty adviser accompanies the students.
>University recognized activities or gatherings sponsored/conducted by students and held after 5pm within the University premises,  and those  scheduled  during non-class  days must  always be attended by the faculty adviser.
•  Materials or written announcements shall be approved by the Office of the Student Affairs and shall be posted on bulletin boards. Remove posters when the activity is over. Avoid posting  materials on walls.
•  Evaluation shall always be apart of an activity.
•  Inviting Speakers
> The adviser of the organization should favorably endorse to OSA Director the invited resource persons, guest lecturers/speakers to participate in any program within the university
Collection of Fees and Fund- Raising Activities
Collection of Fees and Fund-Raising Activities- Student Organization fees imposed by the student council and its sub organizations are used to fund legitimate student activities. Fundraising activities are subject to the following rules and regulations:
• An Organization intending to undertake any form of fund drive shall file a formal application endorsed by the Faculty Adviser to the Office of the Student Affairs atleast two (2) weeks before the activity date. The letter of request/application shall contain the following:
> Nature and purpose of the Activity signed by the adviser and college deans
> The date, time and venue of the activity
> The manner by which the funds shall be used.
> All recognized student organizations are allowed to sponsor one fundraising activity per semester
> A financial statement shall be submitted two (2) weeks after an activity to the Office of the Student Affairs
> Fees for dramatic/cultural presentations and athletic competitions are allowed if sold to the general public
> Whatever  fees  that  are  collected  as permitted herein, must be properly  accounted  for  and recorded. Furnish the Office of the Student Affairs a copy of the audited statement of income and expenditures.
> All collections and donations as well as savings made from any fund drives, shall be deposited with co-depositor in the bank where it shall be kept in a trust fund. Any form of withdrawals could be made for authorized activities.
> Funds of the student organization must be under the custody of the treasurer.
Audit Policy and Program
The Student Organizations Audit Committee (SOAC) is hereby created under the auspices of Junior Philippine Institute of Accountants - Pamantasan ng Lungsod ng Muntinlupa (JPIA-PLMun), and under the supervision of the Office of Students Affairs (OSA). SOAC shall be tasked to promote transparency and accountability among the student organizations of the University by conducting regular audit of the financial statements of student organizations.
The SOAC shall be headed by the Vice Presidents (VP) for Finance and Audit who will act as Co-chairs of the said Committee. The members of the Committee shall be the Executive Officers of JPIA-PLMun.
The Committee shall have the following duties and responsibilities:
•     To conduct pre-audit activities which include asking each organization to issue “Statement of Officers’ Responsibility for the Financial Statements” addressed to Office of Student Affairs and conduct meeting with the Officers of various student organizations to explain the audit flow
•     To compel organizations to pass their financial statements on schedules set by the Office of Student Affairs
•     To acquire original copies of Financial Statements and their supporting documents
•     To conduct inspection of cash books, and organizational files and documents
•     To conduct cash count and inspection of the existence of properties acquired by the Organizations
•     To call the attention of student organizations on matters related to the audit
•     To post names of organizations who do not respond to the audit findings
•     To require organizations, post the audit findings in their bulletin board and official Facebook page (if any) prior to the clearance
•     To issue an audit clearance form to be approved by your Office and which carries the letterhead of the Office of Student Affairs
•     To recommend "an intervention from Office of Student Affairs" for noncompliance and/or discovery of an act that violates any University Policy
•     Any other activities related to conduct of audit
Audit Objectives.
The Committee must accomplish the following objectives related to the items presented in the Financial Statements:
•     Subsection A. Cash
a.     To determine the accuracy of Cash balance presented in the Financial Statement/s
b.     To determine that all cash transactions have been properly recorded and documented
•     Sub. B. Receivable
a.     To determine the accuracy of "Receivables" presented in the Financial Statement/s
b.     To determine that all transactions related to "Receivables" have been properly recorded and documented c.     To determine veracity of the "Receivables" and their collectability
•     Sub. C. Properties
a.     To determine the existence of properties presented on the List of Properties
b.     To determine the completeness of List of Properties
•     Sub. D. Payables
a.     To determine the accuracy of "Payables" presented in the Financial Statements
b.     To determine that all transactions related to "Payables" have been properly recorded and documented. c.     To determine the veracity of "Payables"
•     Sub. E. Income/Revenue
a.     To determine the accuracy of "Income/Revenue" presented in the Income Statement
b.     To determine that the activity where the "Income/Revenue" is earned is properly authorized and/approved
•     Sub. F. Expenses
a.     To determine the accuracy of "Expenses" presented in the Income Statement
b.     To determine that expenses are properly classified as to their nature.
c.     To determine that any cash paid for expenses are properly authorized and liquidated
Audit Procedures
•     The Committee must promulgate appropriate procedures to carry out the audit. The procedures must be approved by the Office of Student Affair
Audit Scope.
•     The audit shall include all financial statements of all accredited student organizations. In no case shall be a student organization accredited by the OSA be exempted from the audit of SOAC
