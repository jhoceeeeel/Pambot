•  Substitution of subjects maybe authorized if the curriculum has been superseded by a new one and the substitution brings the old curriculum in line with the new one.
•  The students shall follow the curriculum he/she enrolled in first year.
Classification of Students
• Based on the Academic Load
 Regular student – a student who is registered for formal academic credits and carries the required full load in his curriculum during a given semester.
 Irregular student – a student who is registered for formal credits but carries less than the required full load in his curriculum during a given semester.
 Working Students - Students who have submitted a copy of their Certificate of present employment and who are allowed to enroll in only 15 units per semester.
• Based on the Year Level
 Froshies– a student who is enrolled in the First Year curriculum, or one who has not completed the prescribed subjects of the First Year curriculum, or one who has completed only 25% of the total number of units required for the entire course.
 Sophomore – a student who is enrolled in the  Second Year curriculum or one who has satisfactorily completed the prescribed subjects in the First Year curriculum or one who has completed more than 25% but less than 50% of the total number of units required for the entire course.
 Junior – a student who is enrolled in the Third Year curriculum, or one who has completed the prescribed subjects of the First Year and Second Year curricula or one who has finished more than 50% but less than 75% of the total number of units required for the entire course.
 Senior – a student who is enrolled in the Fourth Year curriculum, or one who has completed the prescribed subjects of the curricula of the First, Second, and Third Year levels or has completed 75% or more of the total number of units required for the entire course.
• Based on the Admission Status
 Beginning frosh– a student who is admitted after passing the Entrance Test and not enrolled in any academic College subject prior to their enrolment in the University and has submitted to the Guidance Office and the Registrar’s Office all the documents required for formal admission.
 Returning Student (Returnee) – a student is considered for readmission depending on his/her previous
scholastic performance and the availability of slots; must have complied with all the requirements for re- admission.
 Transferee – a student is admitted for an available slot; he/she must have completed studies for one school year; should have submitted satisfactory evidence of their good conduct and academic standing from his/her former school; and they should fulfill all the other requirements of the University.
