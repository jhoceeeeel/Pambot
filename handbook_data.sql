•     Procedure for Complaints and Grievances
  A complaint against student/s should be made in writing duly signed by the aggrieved party and addressed to the University Prefect of Discipline.
  The initial investigation maybe conducted by the Office of the Student Affairs.
  The College Dean shall instruct the Prefect of Discipline to investigate the case and to make appropriate recommendations whenever necessary.
  The Student Discipline Committee (SDC) shall deliberate on the merits of the case, meet with the College Dean regarding its findings and recommends the final action taken on the case to the VPAA.
  The VPAA shall make the appropriate recommendation of the decision to the Office of the President. The President shall immediately implement the decision made.
  As much as possible the SDC, which conducts the deliberation of the case, shall amicably settle the conflict between the parties. If the case prospers, the SDC shall refer the case to the Legal Department of the City where a legal opinion is arrived at and forwarded to the University President. The decision of the University President shall be final and executory.
