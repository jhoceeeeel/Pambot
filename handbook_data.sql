• Absences
 Valid Absences
. An absence is considered valid when a student representing the college or institution in academic, athletic, cultural contests/presentations and activities within or outside as recommended by the Faculty/Chair/OSA and with the Dean’s approval.
. A valid absence is not charged against the student and no grade deduction should apply.
 Invalid Absences
. Absence  due  to  sickness  duly  certified  by  a  doctor,  Parent,  or  guardian  are  not considered valid absences.          However, the faculty member should give a special consideration to absences due to sickness before giving the student a failing grade of 5.
. A student is held responsible for all lessons and               assignments missed during his absence.
 Maximum Number of Absences
. The maximum number of absences allowed a student per semester is 20% of the number of class hours. For instance, 20% of 54 hours is 10.8 or 11 hours. A student is given a grade of 5.00 (Failed) if he incurs absences more than the allowed percentage of class hours.
