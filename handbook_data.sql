Policies and Guidelines of the Student Publications
• The Student Publications Office publishes an official organ which features the latest developments in the Campus. It serves as a vehicle of information of the University. Students are given the opportunity to develop their writing skills and                expression of opinion and journalism using a constructive approach. A faculty adviser serves as the student publication consultant. The Editorial Board and the faculty adviser shall review the articles written by the Editorial staff prior to publication.
• The Student Publication of PLMun adopts the policies of the Campus Journalism Act of 1991, under Republic Act No. 7079,which upholds  student’s freedom of the press and expression of ideas within the bounds of the University vision and mission.(It is Declared policy of the State to upholds and protect the freedom of the press even  at the campus level  and promote the development  and growth of campus journalism as a means of strengthening ethical values, encouraging critical and creative thinking, and developing moral character and personal discipline of the Filipino youth )
• The Student Publication is a sub-unit of the Student Development Section of the Office of the Student Affairs (OSA).
•  SPECIFIC GUIDELINES
The Student Publication has only one (1) adviser chosen by the Office of Student Affairs and Human Resource Office. The adviser shall provide the technical and ethical guidance in the publication procedures. The students who passed the selection process shall compose the Editorial Board.
The Editorial Board is composed of the following:
Students who pass the qualifying examination
Students who are interviewed by a Panel composed of a representative of the school administration, faculty member, a mass media practitioner who is acceptable to both (school administrator and editorial board) and two past editors to be chosen by the outgoing editorial board.
Student Publications Office is part of the Office of the Student Affairs, Director of the OSA serves as the overall Publication consultant, therefore, any articles and newsletter for printing and/or posting  to  social  media must be  submitted  to  the  OSA  Director  signed by  the  adviser  for consultation and approval.
Articles  and  press  releases  concerning  University  must  have  written  authorization  from  the University President upon the recommendation from the OSA Director
 Underground publications for  or against the University or any of its entities is not allowed. The funds of the Student Publication make part of the students’ miscellaneous   fees.
The Student Journalist
> He/She is a bona fide student enrolled during the current semester or term. He/She has passed the qualifying standards for the position of the Student Publication’s Editorial Board.
> He/She maintains a very satisfactory academic standing;
> He/She has acquired one (1) semester apprenticeship under the          Editorial staff;
> He/She has not been subjected to any disciplinary action or he has    never been under probation;
> He/She strictly adheres to the policies of the University and the Office of the Student Affairs.
The Student Publication strictly observes  the following policies and guidelines:
> It adheres to the ethical standards of balanced journalism.
> It carries the constructive views and opinions of the students.
> It serves as a medium of communication for the students to keep them abreast of the current academic and co-curricular programs and activities of the institution.
> It refrains from publishing articles that cause students to go against any staff, faculty and administrator of the University.
> The Student Publication staff should stay in the office during office hours. Time spent beyond the regular office hours is supervised by the adviser.
Steps in the Organization of a Student Publication Staff
> The outgoing editorial board convenes a Screening Committee composed of Representative of the school administration, faculty member, a mass media
practitioner who is acceptable to both (school administrator and editorial board)
and two past editors to be chosen by the outgoing editorial board. (FOR CHECKING)
> A  Selection Committee is organized to pre-qualify the adviser/s and the Editorial Board.
