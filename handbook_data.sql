The Organization
The Board of Regents (BOR) is the governing body of the Pamantasan ng Lungsod ng Muntinlupa (PLMun). It formulates and implements policies deemed necessary for the administration and development of the Institution. The Mayor of the City of
Muntinlupa, as Chairperson, presides over meetings of the Board of Regents (BOR).
The PRESIDENT of the University is the Chief Executive Officer (CEO) of the Institution. She manages the resources of the University and directs its activities. She is responsible for the attainment of the vision, mission and goals of the University.
The VICE PRESIDENT FOR ACADEMIC AFFAIRS supervises the teaching personnel and the academic programs of the University. She conducts curricular revision to set changes that respond to the needs of education, the community, government, business and industry.
The ADMINISTRATIVE OFFICER supervises the administrative personnel and the
FINANCE OFFICER controls and coordinates tasks related to budget, finance and         procurement.
The UNIVERSITY TREASURER takes charge of the safekeeping and releasing of funds as prescribed by law.
The DEAN of each college supervises the teaching personnel in implementing the          academic programs and the co-curricular
activities of the College.
The UNIVERSITY SECRETARY is the custodian of the University records. She assists   the   University   President   in   the preparation of documents  and reports needed in the Board of Regents’ (BOR) meetings  and in the formulation of administrative policies, rules and regulations.
The UNIVERSITY REGISTRAR keeps the records of students, sets and implements enrolment procedures in accordance with policies and standards set by the Board of Regents (BOR) and the Commission on Higher Education (CHED).
The UNIVERSITY LIBRARIAN supervises and maintains the operation of the University Library. He is responsible for the improvement of the Library and the Multi-media Center.
The SECURITY UNIT maintains peace and order. It ensures the safety of the students and employees. It oversees and protects University property. It implements security measures during academic and non-academic activities of the University and the colleges.
The COMMITTEE ON DECORUM AND INVESTIGATION (CODI) investigates cases forwarded by the following offices: the College, the Guidance Office and the Office of the Student Affairs. The results of the Committee investigation are forwarded to the Office of the University President for appropriate action.
