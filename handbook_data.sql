>Exert his utmost to develop his potentialities for service, particularly by undergoing an education suited to his abilities, in order that he may become an asset to his family and to society.
>Uphold the academic integrity of the school, endeavor to achieve academic             excellence and abide by the rules and regulations governing his academic               responsibilities and moral integrity.
>Promote and maintain the peace and tranquility of the school by observing the rules and discipline, and by exerting efforts to attain harmonious relationships with fellow students, the teaching and academic staff and other school personnel.
>Participate actively in civic affairs and in the promotion of the general welfare,       particularly in the social, economic and cultural development of his community and in the attainment of a just, compassionate and orderly society.
>Exercise his rights responsibly in the knowledge that he is answerable for any       infringement or violation
of the public welfare and of the rights of others.
Student Organizations
Protects the interest of the students and posters good relationship among students, faculty members and administrative staff
• The university creates a friendly environment for the students to develop their ability, skills and creativity and to guide them to become responsible citizens of the community.            Student organizations are part of the co- curricular and extra-curricular activities that will cater to student’s social,cultural, educational, moral and spiritual development.
•  Student activities form part of the informal learning process in the University. They should not interfere with academic studies but should instead contribute to the total balanced   formation of students.
•  Student organizations ’ principles and philosophy should align with that of the University Vision and Mission
•  Student groups shall have the right to seek accreditation/recognition subject to standards set by the Office of the Student Affairs (OSA) of the Pamantasan ng Lungsodng Muntinlupa.
•  Student Organizations shall enjoy their own leadership structure, set their own directions and goals and shall plan and manage their resources. They shall have adviser/s who shall guide them in the right directions.
• The Director/Dean of the Office of the Student Affairs (OSA) sets the parameters within which the organizations shall operate and sits as ex-officio adviser of the highest student organization.
•  Student Organizations shall be recognized upon the submission of objectives, constitution and by-laws and profile of officers.
• Renewal of terms of all organizations is done every start of the school year upon  submission of Financial Statements and list of new officers. Failure to submit the necessary documents shall be considered a ground for non-renewal of the organization.
• There shall be no duplication of objectives among student organizations.
•  Students who are running for positions in the University Student Council, Major and Minor Organizations must:
 be of good moral character
 have shown leadership capabilities
 not hold any major position in other Organizations
 have a two-semester residency prior to his application with the exception of First Year Level Representative.
 have a Grade Point Average (GPA) of 2.0 for the last two (2) semesters
 have no failing grades, INC, UW in all academic semesters
 must not be under any form of probation upon his application
 must be favorably recommended by his adviser.
• The UNIVERSITY STUDENT COUNCIL (USC)
 The USC is the official representative of the entire student body and is given     recognition by the PLMun.
It is democratic and autonomous.
 The USC has its own set of officers, both elected and / or appointed.
 The USC has the right to determine the policies and programs on matters within its jurisdiction guided by its duly ratified Constitution and policies.
 The USC President shall automatically be a member of the Board of Regents (BOR).
 The USC President/student leaders shall be assured of a security of tenure in their positions for the duration of their term unless removed due to academic cases, disciplinary cases or impeachment.
