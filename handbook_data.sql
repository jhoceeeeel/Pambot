HISTORICAL BACKGROUND OF THE PAMANTASAN NG LUNGSOD NG MUNTINLUPA (PLMUN)
The establishment of a local university in the Municipality started as a dream of former Mayor Ignacio “Toting” Bunye who viewed education as potent tool for transforming society for the better. Upon his assumption of office in 1986, he included the objective of organizing and establishing an institution of higher learning in the Ten Point Agenda of his administration.
During his term as Councilor and Chairman of the Committee on Education, Rufino B. Joaquin sponsored the move to organize
and establish the  Muntinlupa Polytechnic College (MPC). On May 29, 1991 the Municipal Council of        Muntinlupa passed
Resolution No. 91- 191 and Resolution No. 91- 192 officially creating a government run college – the Muntinlupa Polytechnic College (MPC)
Muntinlupa Polytechnic College (MPC) started with 309 students while using one of the high school buildings of the Pedro E. Diaz High School in Alabang, Muntinlupa City. As its enrollment of students increased, the Muntinlupa Polytechnic College   (MPC) was relocated to the second floor of the Contessa Bulding in Barangay Poblacion, Muntinlupa City where all of its the  offices and classrooms were air- conditioned.
Courses offered then at the Muntinlupa Polytechnic College (MPC) were as follows:  Bachelor of Arts in Psychology,
Bachelor of Science in Commerce, Bachelor of Science in Accountancy, Bachelor of Elementary Education, Bachelor of
Secondary Education,  Bachelor of Science in Criminology, Bachelor of Science in Industrial Technology, and two-year courses in Computer Science and Secretarial Science.
Imbued with the desire to be in close coordination with the National Government in providing higher education, the Muntinlupa Polytechnic College (MPC) established a linkage with the Commission of Higher Education (CHED-NCR).
The rapid growth of the student population at the Muntinlupa Polytechnic College (MPC) ushered in the need for more changes.
In 1995, due to the high educational standards and numerous courses it offered, the number of students increased. This prompted  a need for more space and facilities. This served as a challenge to Muntinlupa which by then in 1995 had just been elevated to the status of a City.
In response to the challenge, in March 1988, the Muntinlupa Polytechnic College (MPC) moved out of the Contessa Building to
its own new building in a bigger campus with better facilities. The City Government had a new two-storey building constructed and completed alongside the Muntinlupa National High School, in a two hectare site in the NBP Reservation Compound,
Poblacion, Muntinlupa City.
Upon his assumption of office in 1998, Mayor Jaime R. Fresnedi had education as a priority in the Eight Point Program of his   Administration. Compulsory Education was extended to everyone, especially, to the marginalized sector. In 2002, the new four storey Academic Building was completed to house the library and Auditorium and to provide additional classrooms in its third  and fourth floors. The new Academic Building further enhanced the ability of the Muntinlupa Polytechnic College to produce   highly educated young residents who would eventually contribute to the development of themselves, their loved ones and the    City of Muntinlupa.
Former Dean Enrico Vivar led the movement to convert the Muntinlupa Polytechnic College into a local university. Atty. Raul R. Corro, then Councilor and Chairman of the Committee on Education, sponsored City Ordinance No. 03-089 converting the
Muntinlupa Polytechnic College to a Pamantasan ng Lungsod ng Muntinlupa (PLMun) in March, 2003 during the 67th session.
The Pamantasan ng Lungsod ng Muntinlupa has three buildings: the Main Building, the Annex Building  and the Administration Building.
The PLMun has attained recognition by CHED as leading Higher Educational Institution (HEI) offering of various relevant courses such as: General Education, Criminal Justice, Teacher Education, Business Administration, Arts and Sciences,
Information Technology and Computer Studies.
As Education Mayor, during his term (1997—2006), Mayor Jaime R. Fresnedi and the City Council approved the Iskolar ng Bayan Ordinance No. 98-008 which provided additional scholarship incentives for indigent but deserving PLMun students.
Seeing the need to sustain empowerment among young Muntinlupenos, the 7th SangguniangPanglungsod passed a resolution to increase student financial assistance, hence Honorable Mayor Jaime R. Fresnedi, passed a decree on October 13, 2013, creating  Ordinance No.13-010 (Amending Ordinance No.08-042) which provided for even more incentives for scholars.
The Board of Regents and management are continuously making sure that the PLMun and its faculty provide quality and relevant education that would produce graduates who are creative, productive, socially responsible and locally and globally competitive.
